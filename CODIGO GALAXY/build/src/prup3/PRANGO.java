    public PRANGO() {
        initComponents();
        lista = new PUNTOS[5];
        contador=0;
        
        
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.addColumn("NOMBRE");
        tabla.addColumn("PUNTUACION");
        
        
        String nombre = GUARDADO.name;
        String puntos = OBPUNTOS.conversion;
        
        for (int i = 0; i < lista.length; i++) {
        
            PUNTOS lvl = new PUNTOS(nombre,puntos);
            lista[contador]= lvl;
            contador++;
            
            tabla.addRow(new Object[] {lista[i].getNombre(),lista[i].getPuntos()});
        }
        
        
        
        
        Dtabla.setModel(tabla);
        
    }

